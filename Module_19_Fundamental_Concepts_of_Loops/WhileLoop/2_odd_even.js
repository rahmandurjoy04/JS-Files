/*
Subtask-1:
Find all the odd numbers from 61 to 100.
*/
console.log("All the odd numbers from 61 to 100 are : ")
let i = 61;
while (i <= 100){
    if(i % 2 !== 0){
        console.log(i);
    }
    i++
}


